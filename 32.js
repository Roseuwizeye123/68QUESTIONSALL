//Create a function that receives two numbers and returns the bigger number.
function biggerNumber(num1, num2) {
    if (num1 > num2) {
        return num1;
    } else {
        return num2;
    }
}

// Example usage:
console.log(biggerNumber(5, 10)); 
console.log(biggerNumber(20, 7)); 
console.log(biggerNumber(-3, -8)); 
