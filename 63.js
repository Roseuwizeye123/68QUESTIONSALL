//Write a function that converts a temperature from Celsius to Fahrenheit.
function celsiusToFahrenheit(celsius) {
    return (celsius * 9/5) + 32;
}

// Example usage:
console.log(celsiusToFahrenheit(0)); // Output: 32°F (freezing point of water)
console.log(celsiusToFahrenheit(100)); // Output: 212°F (boiling point of water)
